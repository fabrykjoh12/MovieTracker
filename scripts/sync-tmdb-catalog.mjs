import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const token = process.env.TMDB_READ_ACCESS_TOKEN?.trim();
if (!token) {
  throw new Error(
    "TMDB_READ_ACCESS_TOKEN is required. Add it to .env.local or GitHub Actions secrets.",
  );
}

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const seedPath = resolve(root, "catalog/tmdb-seeds.json");
const outputPath = resolve(root, "src/generated/tmdbCatalog.ts");
const seeds = JSON.parse(await readFile(seedPath, "utf8"));

if (!Array.isArray(seeds) || seeds.length === 0) {
  throw new Error("catalog/tmdb-seeds.json must contain at least one title.");
}

const headers = {
  Accept: "application/json",
  Authorization: `Bearer ${token}`,
};

async function fetchJson(url) {
  const response = await fetch(url, {
    headers,
    signal: AbortSignal.timeout(15_000),
  });
  if (!response.ok) {
    throw new Error(
      `TMDB returned ${response.status} for ${new URL(url).pathname}.`,
    );
  }
  return response.json();
}

const configuration = await fetchJson(
  "https://api.themoviedb.org/3/configuration",
);
const images = configuration.images;
if (!images?.secure_base_url) {
  throw new Error(
    "TMDB configuration did not include a secure image base URL.",
  );
}

const chooseSize = (available, preferred) =>
  available?.includes(preferred) ? preferred : "original";
const posterSize = chooseSize(images.poster_sizes, "w500");
const backdropSize = chooseSize(images.backdrop_sizes, "w1280");
const imageUrl = (path, size) =>
  path ? `${images.secure_base_url}${size}${path}` : null;
const yearFrom = (date) => {
  const year = Number.parseInt(String(date ?? "").slice(0, 4), 10);
  return Number.isFinite(year) ? year : null;
};

const entries = {};
for (const seed of seeds) {
  if (
    !seed ||
    typeof seed.localId !== "string" ||
    typeof seed.title !== "string" ||
    !["movie", "tv"].includes(seed.mediaType) ||
    !Number.isInteger(seed.providerId)
  ) {
    throw new Error(
      "Every TMDB seed needs localId, title, mediaType, and providerId.",
    );
  }

  const details = await fetchJson(
    `https://api.themoviedb.org/3/${seed.mediaType}/${seed.providerId}?language=en-US`,
  );
  if (details.id !== seed.providerId) {
    throw new Error(`TMDB returned the wrong title for ${seed.localId}.`);
  }
  const providerTitle =
    seed.mediaType === "movie" ? details.title : details.name;
  if (
    providerTitle?.toLocaleLowerCase("en-US") !==
    seed.title.toLocaleLowerCase("en-US")
  ) {
    throw new Error(
      `TMDB ID ${seed.providerId} resolved to ${providerTitle ?? "an unnamed title"}, not ${seed.title}.`,
    );
  }

  const runtime =
    seed.mediaType === "movie"
      ? details.runtime
      : details.episode_run_time?.find((value) => value > 0);
  entries[seed.localId] = {
    providerId: seed.providerId,
    mediaType: seed.mediaType,
    posterUrl: imageUrl(details.poster_path, posterSize),
    backdropUrl: imageUrl(details.backdrop_path, backdropSize),
    synopsis:
      typeof details.overview === "string" && details.overview.trim()
        ? details.overview.trim()
        : null,
    runtime: Number.isFinite(runtime) && runtime > 0 ? runtime : null,
    year: yearFrom(
      seed.mediaType === "movie"
        ? details.release_date
        : details.first_air_date,
    ),
    genres: Array.isArray(details.genres)
      ? details.genres
          .map((genre) => genre?.name)
          .filter((name) => typeof name === "string" && name.length > 0)
      : [],
  };
}

const catalog = {
  generatedAt: new Date().toISOString(),
  entries,
};
const source = `import type { TmdbCatalog } from "../catalog/tmdb";\n\n// Generated by npm run catalog:sync. Do not edit by hand.\nexport const tmdbCatalog: TmdbCatalog = ${JSON.stringify(catalog, null, 2)};\n`;
await writeFile(outputPath, source, "utf8");

console.log(`Synced ${Object.keys(entries).length} curated titles from TMDB.`);
